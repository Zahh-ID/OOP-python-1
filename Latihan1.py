class Mahasiswa:
    def __init__ (self, nama, nim, prodi):
        self.nama = nama
        self.nim = nim
        self.prodi = prodi
        

    def tampilkan_info(self):
        print (f"nama: {self.nama}")
        print (f"nim: {self.nim}")
        print (f"prodi: {self.prodi}")

mhs1 = Mahasiswa("Nazwa Hilmia Zida", "240533604545", "PTI")

mhs1.tampilkan_info()
